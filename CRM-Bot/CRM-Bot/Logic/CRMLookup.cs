using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;

namespace CRM_Bot.Logic
{
    public class CRMLookup
    {
        public string CRMLookupDialogue(string tag)
        {
            switch (tag)
            {
                case "Meeting Minutes":
                    return "";

                case "Last Order":
                    return "";

                case "Any Dues":
                    return "";

                case "Key Contacts":
                    return "";

                default:
                    return "";
            }
        }
    }
}